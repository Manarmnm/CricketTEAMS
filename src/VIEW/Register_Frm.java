    private void btnAdd1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd1ActionPerformed
        // TODO add your handling code here:
        if(isValidate()){
        String FName= txtfname.getText().trim(); 
        String LName= txtlname.getText().trim(); 
        String UName= txtuname.getText().trim(); 
        String Address= txtaddress.getText().trim(); 
        String nic= txtnic.getText().trim(); 
        String dob= txtdob.getText().trim(); 
        String tel= txttelno.getText().trim();
        String pwd= txtPwd.getText().trim(); 
        String vpwd= txtVPwd.getText().trim(); 
        String Category=cmbcategory.getSelectedItem().toString();
        String Email=txtemail.getText();
        String Job= txtjob.getText();
       // String town= cmbtown.getSelectedItem().toString();
        String area= txtcity.getText();
        String Gender="";
        
         if(rdoMale.isSelected()){
            Gender="Male";
        }else 
            if(rdofemale.isSelected()){
                Gender="Female";
            }
        
        
         
        
        if(pwd.equals(vpwd)){
            Login log=new Login(FName, LName, UName, Address, Gender, dob, nic, tel, Category, pwd, area,Email,Job);
            
            int result=logDB.addLogin(log);
            
            if(result>0){
                JOptionPane.showMessageDialog(rootPane, "New Login is added");
            }else{
                JOptionPane.showMessageDialog(rootPane, "New Login is not added");
            }  
        }else{
            JOptionPane.showMessageDialog(rootPane, "Password is not matching with confirm password");
        }
     }
    }//GEN-LAST:event_btnAdd1ActionPerformed

