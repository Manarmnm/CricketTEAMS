 private boolean isValidate(){ //Validate change into isValidate
       // boolean flag=true;
       // text boxes can't be empty
        if (txtfname.getText().equals("")) //flag=false;
        {
            JOptionPane.showMessageDialog(rootPane, "First name is a requied field");  //help message
            txtfname.requestFocusInWindow();
            return false;
        }
        if (txtaddress.getText().equals("")) //flag=false;
        {
            JOptionPane.showMessageDialog(rootPane, "Address is a requied field");  //help message
            txtaddress.requestFocusInWindow();
            return false;
        }
        if (txttelno.getText().equals("")) //flag=false;
        {
            JOptionPane.showMessageDialog(rootPane, "Contact Details is a requied field");  //help message
            txttelno.requestFocusInWindow();
            return false;
        }
        if (txtuname.getText().equals("")) //flag=false;
        {
            JOptionPane.showMessageDialog(rootPane, "User Name is a requied field");  //help message
            txtuname.requestFocusInWindow();
            return false;
        }
         if (txtemail.getText().equals("")) //flag=false;
        {
            JOptionPane.showMessageDialog(rootPane, "Email ID is a requied field");  //help message
            txtemail.requestFocusInWindow();
            return false;
        }
        return true;
 }
